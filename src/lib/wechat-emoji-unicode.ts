const REGEX = /\[([^\]\r\n]{1,6})\]/g;

/** Convert [text] codes to Unicode emoji for Excel cells */
export function wechatToUnicode(text: string): string {
  if (!text.includes('[')) return text;
  return text.replace(REGEX, (match, name: string) => MAP[name] ?? match);
}
